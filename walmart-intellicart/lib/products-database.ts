// Helper functions for product operations
export function getProductById(id: string): Product | undefined {
  return PRODUCTS_DATABASE.find((product) => product.id === id)
}

export function getProductsByCategory(category: string): Product[] {
  return PRODUCTS_DATABASE.filter((product) => product.category.toLowerCase() === category.toLowerCase())
}

export function getProductsByStoreSection(section: string): Product[] {
  return PRODUCTS_DATABASE.filter((product) => product.storeSection.toLowerCase() === section.toLowerCase())
}

export function searchProducts(query: string): Product[] {
  const searchTerm = query.toLowerCase()
  return PRODUCTS_DATABASE.filter(
    (product) =>
      product.name.toLowerCase().includes(searchTerm) ||
      product.description.toLowerCase().includes(searchTerm) ||
      product.tags.some((tag) => tag.toLowerCase().includes(searchTerm)) ||
      product.brand.toLowerCase().includes(searchTerm) ||
      product.category.toLowerCase().includes(searchTerm),
  )
}

export function getProductsByPriceRange(minPrice: number, maxPrice: number): Product[] {
  return PRODUCTS_DATABASE.filter((product) => product.price >= minPrice && product.price <= maxPrice)
}

export function getProductsByDietaryRestrictions(restrictions: string[]): Product[] {
  return PRODUCTS_DATABASE.filter((product) => {
    return restrictions.every((restriction) => {
      switch (restriction.toLowerCase()) {
        case "organic":
          return product.organic === true
        case "gluten-free":
          return product.glutenFree === true
        case "vegan":
          return product.vegan === true
        case "vegetarian":
          return product.vegetarian === true
        default:
          return true
      }
    })
  })
}

export function getFeaturedProducts(limit = 10): Product[] {
  return PRODUCTS_DATABASE.filter((product) => product.rating >= 4.5)
    .sort((a, b) => b.reviewCount - a.reviewCount)
    .slice(0, limit)
}

export function getTopRatedProducts(limit = 10): Product[] {
  return PRODUCTS_DATABASE.sort((a, b) => b.rating - a.rating).slice(0, limit)
}

export function getProductsOnSale(): Product[] {
  return PRODUCTS_DATABASE.filter((product) => product.originalPrice && product.originalPrice > product.price)
}

export function getProductsByBrand(brand: string): Product[] {
  return PRODUCTS_DATABASE.filter((product) => product.brand.toLowerCase() === brand.toLowerCase())
}

export function getAllCategories(): string[] {
  return [...new Set(PRODUCTS_DATABASE.map((product) => product.category))]
}

export function getAllBrands(): string[] {
  return [...new Set(PRODUCTS_DATABASE.map((product) => product.brand))]
}

export function getProductRecommendations(productId: string, limit = 5): Product[] {
  const product = getProductById(productId)
  if (!product) return []

  // Get products from same category with similar tags
  return PRODUCTS_DATABASE.filter(
    (p) => p.id !== productId && (p.category === product.category || p.tags.some((tag) => product.tags.includes(tag))),
  )
    .sort((a, b) => b.rating - a.rating)
    .slice(0, limit)
}
